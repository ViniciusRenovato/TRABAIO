    private void CarregaLista(Agendamento agendamento){
        
        
        
        
        
        lista = new LinkedList<>();
               
         lista.add(agendamento);
        
        
        String[] columnNames = new String[]{
          "ID","Nome", "Tipo de Pagamento", "Valor total"
                
        };
        Object[][] data = new Object[lista.size()][columnNames.length];
        for (int i = 0; i < lista.size(); i++) {
            data[i][0] = lista.get(i).getId();
            data[i][1] = lista.get(i).getCadastroCliente().getNome();
            data[i][2] = lista.get(i).getTipoPagamento();
            data[i][3] = lista.get(i).getServicos().getValor();
           
            
         
        }
        tablerecibo.setModel(new DefaultTableModel(data, columnNames));
    } 

